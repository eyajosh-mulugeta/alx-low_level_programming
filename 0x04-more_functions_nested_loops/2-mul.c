/**
 * mul - a function that finds the product of two integers.
 * @a: a parameter which holds the value of the first integer.
 * @b: a parameter which holds the value of the second integer.
 * Return: returns the product of the 2 integers.
 */
int mul(int a, int b)
{
return (a * b);
}
